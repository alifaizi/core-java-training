package com.loops.practice;

public class WhileLoop {

	public static void main(String[] args) {
		
		// Increment
		
		int num = 1;

		while (num <= 10) {
		    System.out.println("Number: " + num);
		    num++;
		}
		
		System.out.println("-------------------------------------------------------------");
		
		// Decrement
		
		int num1 = 10;

		while (num1 >= 1) {
		    System.out.println("Number: " + num1);
		    num1--;
		}
		
	}

}
