package com.operators.practice;

public class LogicalOperator {
	
	public static void main(String[] args) {
		
		boolean num1 = false;
		boolean num2 = true;
		
		System.out.println((num1 && num2));
		
		System.out.println((num1 || num2));
		
		System.out.println(!(num1 && num2));
		
		System.out.println(!(num1 || num2));
		
	}

}
